static int ImGuiColFromKey(const std::string& key) {
    if (key == "WindowBg")             return ImGuiCol_WindowBg;
    if (key == "ChildBg")              return ImGuiCol_ChildBg;
    if (key == "PopupBg")              return ImGuiCol_PopupBg;
    if (key == "Border")               return ImGuiCol_Border;
    if (key == "Text")                 return ImGuiCol_Text;
    if (key == "TextDisabled")         return ImGuiCol_TextDisabled;
    if (key == "FrameBg")              return ImGuiCol_FrameBg;
    if (key == "FrameBgHovered")       return ImGuiCol_FrameBgHovered;
    if (key == "FrameBgActive")        return ImGuiCol_FrameBgActive;
    if (key == "TitleBg")              return ImGuiCol_TitleBg;
    if (key == "TitleBgActive")        return ImGuiCol_TitleBgActive;
    if (key == "TitleBgCollapsed")     return ImGuiCol_TitleBgCollapsed;
    if (key == "Button")               return ImGuiCol_Button;
    if (key == "ButtonHovered")        return ImGuiCol_ButtonHovered;
    if (key == "ButtonActive")         return ImGuiCol_ButtonActive;
    if (key == "Header")               return ImGuiCol_Header;
    if (key == "HeaderHovered")        return ImGuiCol_HeaderHovered;
    if (key == "HeaderActive")         return ImGuiCol_HeaderActive;
    if (key == "Tab")                  return ImGuiCol_Tab;
    if (key == "TabHovered")           return ImGuiCol_TabHovered;
    if (key == "TabSelected")          return ImGuiCol_TabSelected;
    if (key == "SliderGrab")           return ImGuiCol_SliderGrab;
    if (key == "SliderGrabActive")     return ImGuiCol_SliderGrabActive;
    if (key == "ScrollbarBg")          return ImGuiCol_ScrollbarBg;
    if (key == "ScrollbarGrab")        return ImGuiCol_ScrollbarGrab;
    if (key == "ScrollbarGrabHovered") return ImGuiCol_ScrollbarGrabHovered;
    if (key == "ScrollbarGrabActive")  return ImGuiCol_ScrollbarGrabActive;
    if (key == "CheckMark")            return ImGuiCol_CheckMark;
    if (key == "TextSelectedBg")       return ImGuiCol_TextSelectedBg;
    if (key == "Separator")            return ImGuiCol_Separator;
    if (key == "SeparatorHovered")     return ImGuiCol_SeparatorHovered;
    if (key == "SeparatorActive")      return ImGuiCol_SeparatorActive;
    if (key == "ResizeGrip")           return ImGuiCol_ResizeGrip;
    if (key == "ResizeGripHovered")    return ImGuiCol_ResizeGripHovered;
    if (key == "ResizeGripActive")     return ImGuiCol_ResizeGripActive;
    return -1;
}

// Apply per-slot custom color overrides from config on top of the current style.
void ApplyCustomColorsOverlay(
    const std::map<std::string, std::array<float, 4>>& customColors) {
    ImGuiStyle& style = ImGui::GetStyle();
    for (const auto& [key, rgba] : customColors) {
        int idx = ImGuiColFromKey(key);
        if (idx >= 0 && idx < ImGuiCol_COUNT) {
            style.Colors[idx] = ImVec4(rgba[0], rgba[1], rgba[2], rgba[3]);
        }
    }
}
